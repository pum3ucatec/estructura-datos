using System;

class Program
{
    static void Main()
    {
        int i = 1;

        // Bucle for para contar del 1 al 10
        for (i = 1; i <= 10; i++)
        {
            Console.WriteLine(i);
        }

        Console.WriteLine("¡Fin del conteo!");
        Console.ReadKey(); // Mantiene la consola abierta
    }
}
