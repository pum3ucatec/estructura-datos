using System;

class Programa
{
    static void Main()
    {
        // Contar del 1 al 10 usando un bucle for
        for (int i = 1; i <= 10; i++)
        {
            Console.WriteLine(i);
        }

        Console.WriteLine("Presiona cualquier tecla para salir...");
        Console.ReadKey();
    }
}
